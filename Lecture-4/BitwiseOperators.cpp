#include <iostream>
using namespace std;

int main() {

	cout << "5&7: " << (5 & 7) << endl;
	cout << "5|3: " << (5 | 3) << endl;
	cout << "5^7: " << (5 ^ 7) << endl;
	cout << "5^10: " << (5 ^ 10) << endl;
	cout << "5^5: " << (5 ^ 5) << endl;
	cout << "0^5: " << (0 ^ 5) << endl;


	return 0;
}





