#include <iostream>
using namespace std;

int main() {
	// 1 -> 00000000000000000000000000000101

	cout << "(1 << 3): " << (1 << 3) << endl;
	cout << "(1 << 4): " << (1 << 4) << endl;
	cout << "(2 << 5): " << (2 << 5) << endl;
	cout << "(5 << 5): " << (5 << 5) << endl;
	cout << "(16 << 5): " << (16 << 5) << endl;
	cout << "(16 * 32): " << (16 * 32) << endl;

	return 0;
}
















